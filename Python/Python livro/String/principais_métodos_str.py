print("maranaca".capitalize()) # .capitalize coloca a primeira letra como maiúscula 
print("maracana".count("a")) # .count exibe quantas letras possui na string 
print("maracana".startswith("m")) # .startswitch verifica se a primeira letra da string é "m"
print("maracana".endswith("z")) # .endswitch verifica se a ultima letra da string é "z"
print("copa de 2014".split(" ")) # .split utilizado para separar a string por partes
print("_2_".join(["copa", "de", "2014"])) # .join adiciona o que estiver antes do comando neste caso "_2_" 
print("copa de 2014".replace("2014", "2018")) # .replace remove o valor indicado da string e adiciona o valor indicado a string